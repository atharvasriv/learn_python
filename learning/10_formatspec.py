# format specifiers -> {value:flags} in a f-string
#                      formats a value based on the flags inserted

# .(number)f -> round to (number) decimal places (fixed point)
# :(number) -> allocate that many spaces
# :03 -> allocate and zero pad that many spaces
# :< -> left justify
# :> -> right justify
# :^ -> center align
# :+ -> use a plus sign to indicate positive value
# := -> place sign to leftmost position
# :  -> insert a space before positive numbers
# :, -> comma separator (basically place value separator)

# flags can be used together as well (combination of flags)
